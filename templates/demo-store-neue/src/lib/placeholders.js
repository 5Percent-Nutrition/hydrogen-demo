export function withPlaceholder(placeholder, heros) {
  switch (placeholder) {
    case 'HEROS': {
      return heros.map((hero, index) => {
        if (hero?.title?.value) {
          return hero;
        }

        // hero placeholder
        const {
          id,
          title,
          byline,
          cta,
          handle,
          spread,
          spreadSecondary,
          height,
          top,
        } = PLACEHOLDERS.HEROS[index];

        // prioritize metafield if available, else collection values otherwise placeholder values
        const byLine =
          hero?.byLine || hero?.descriptionHtml
            ? {value: hero.descriptionHtml}
            : byline;

        const heading =
          hero?.heading || hero?.title ? {value: hero.title} : title;

        // merge hero placeholder with collection data
        return {
          heading,
          byLine,
          cta: hero?.cta || cta,
          handle: hero?.handle || handle,
          id: hero?.id || id,
          spread: hero?.spread || spread,
          spreadSecondary: hero?.spreadSecondary || spreadSecondary,
          height,
          top,
        };
      });
    }

    case 'PRODUCT':
    case 'PRODUCT_INFO':
      return PLACEHOLDERS[placeholder];

    default:
      return null;
  }
}
